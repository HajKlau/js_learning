let liczba3 = 15;
let liczba4 = 20;
let operatorr = "+";

if (operatorr == "+") {
    console.log("Wynik dodawania: " + (liczba3 + liczba4));
} 
else if (operatorr == "-") {
    console.log("Wynik odejmowania: " + (liczba3 - liczba4));
}
else if (operatorr == "*") {
    console.log("Wynik mno≈ºenia: " + (liczba3 * liczba4));
}
else if (operatorr == "/") {
    console.log("Wynik dzielenia: " + (liczba3 / liczba4));
} 
else {
    console.log("Nieznany operator")
}